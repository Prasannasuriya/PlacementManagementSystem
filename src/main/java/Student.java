public class Student {

    int id;
    String name;
    String department;
    double cgpa;
    String placed;

    public Student(int id, String name, String department,
            double cgpa, String placed) {

        this.id = id;
        this.name = name;
        this.department = department;
        this.cgpa = cgpa;
        this.placed = placed;
    }
}